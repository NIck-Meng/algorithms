package practice;

public class RemoveDuplicatesfromSortedArray {

//    Given nums = [1,1,2],
//    Your function should return length = 2, with the first two elements of nums being 1 and 2 respectively.
    private int solution1(int[] vec ){
        int n=0;
        for(int i:vec){
            for(int j:vec){
                if(i!=j) n+=1;
            }
        }


        return 9;
    }


}
